#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	size_t	len;
	size_t	i;

	len = ft_strlen(s);
	i = -1;
	while (++i < len)
		f(i, &s[i]);
	return ;
}
